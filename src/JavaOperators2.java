
public class JavaOperators2 {


    public static void main(String args[]){
       int num1 = 10;
       //int num2 = 5;
        int num2 = 15;
        //int num2 = 55;
       int num3 = 25;
       //and, or operator
//num2 < num1
//num2 < num3
        //and -> both statements should be true
        //or -> any one of the statements should be true
        System.out.println(num2<num1 && num2 < num3);
        System.out.println(num2<num1 || num2 < num3);

    }
}
